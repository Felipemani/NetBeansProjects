    private void btnLeiloarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLeiloarActionPerformed
        System.out.println("Leiloar");
        registrarCliente();

        String nome = txtNome.getText();
        String descricao = txtDescricao.getText();
        String precoString = txtPreco.getText();
        precoString = precoString.replace(",", ".");
        double preco = Double.parseDouble(precoString);
        int tempo = Integer.parseInt(txtTempo.getText());

        Produto produto = new Produto(nome, descricao, preco, tempo, cliente.getId());

        try {
            leiloeiroInterface.incluirProduto(produto);
        } catch (RemoteException ex) {
            Logger.getLogger(Leilao.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtNome.setText("");
        txtDescricao.setText("");
        txtPreco.setText("");
        txtTempo.setText("");

        lblLog.setText(lblLog.getText() + "Produto inserido no servidor");
    }//GEN-LAST:event_btnLeiloarActionPerformed

    private void btnLanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLanceActionPerformed
        registrarCliente();

        //dar lance num produto
        int linha = jTable1.getSelectedRow();
        if (produtos.get(linha).getClienteID() != cliente.getId() && produtos.get(linha).getLanceID() != cliente.getId()) {

            String precoString = txtLance.getText();
            precoString = precoString.replace(",", ".");
            double preco = Double.parseDouble(precoString);
            try {
                leiloeiroInterface.lance(produtos.get(linha).getID(), preco, cliente.getId(), clienteRMI.getClienteInterface());
            } catch (RemoteException ex) {
                System.out.println("Erro em dar lance");
                ex.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Operação não pode ser realizada. Esse produto é seu ou o último lance é seu!", "ALERTA", JOptionPane.WARNING_MESSAGE);
        }
        
        txtLance.setText("");
        atualizarSalaoLeilao();
    }//GEN-LAST:event_btnLanceActionPerformed

    private void lbMeuslLancesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbMeuslLancesMouseClicked
        tela = 3;
        lblStatus.setText("Meus Lances");

        tblMeusLances.setVisible(true);
        lblDescricao.setVisible(true);

        txtDescricao.setVisible(false);
        lblNome.setVisible(false);
        txtNome.setVisible(false);
        lblDescricaoCadastro.setVisible(false);
        lblPreco.setVisible(false);
        txtPreco.setVisible(false);
        lblLance.setVisible(false);
        txtLance.setVisible(false);
        lblTempo.setVisible(false);
        txtTempo.setVisible(false);
        btnLance.setVisible(false);
        btnLeiloar.setVisible(false);
        lblDescricaoSalao.setVisible(false);

        meusProdutos = new ArrayList();
        subscribe = new ArrayList();

        //buscar primeiro os meus produtos
        try {
            meusProdutos = leiloeiroInterface.meusProdutos(cliente.getId());

            //buscar produtos de meu interesse
        } catch (RemoteException ex) {
            System.out.println("Erro no buscar meus produtos");
            ex.printStackTrace();
        }

        //buscar produtos do subscribe
        try {
            subscribe = leiloeiroInterface.produtosSubscribe(cliente.getId());

            //buscar produtos de meu interesse
        } catch (RemoteException ex) {
            System.out.println("Erro no buscar meus subscribes");
            ex.printStackTrace();
        }

        if (model != null) {
            for (int i = model.getRowCount() - 1; i >= 0; i--) {
                model.removeRow(i);
            }
        }

        model = (DefaultTableModel) jTable1.getModel();

        String[] linha;
        for (Produto p : meusProdutos) {
            linha = new String[]{Integer.toString(p.getID()), p.getNome(), Double.toString(p.getLance())};
            model.addRow(linha);
        }
        linha = new String[]{"SUBSCRIBES", "-----", "----"};
        model.addRow(linha);

        for (Produto p : subscribe) {
            linha = new String[]{Integer.toString(p.getID()), p.getNome(), Double.toString(p.getLance())};
            model.addRow(linha);
        }
    }//GEN-LAST:event_lbMeuslLancesMouseClicked

