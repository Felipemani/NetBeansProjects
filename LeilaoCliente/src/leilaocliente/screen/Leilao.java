    private void atualizarSalaoLeilao() {
        produtos = new ArrayList();
        try {
            //buscar produtos à venda
            produtos = leiloeiroInterface.listaProdutos();
        } catch (RemoteException ex) {
            System.out.println("Erro no buscar lista de produtos à venda");
            ex.printStackTrace();
        }

        if (model != null) {
            for (int i = model.getRowCount() - 1; i >= 0; i--) {
                model.removeRow(i);
            }
        }

        model = (DefaultTableModel) jTable1.getModel();

        for (Produto p : produtos) {
            String[] linha = new String[]{Integer.toString(p.getID()), p.getNome(), Double.toString(p.getLance())};
            model.addRow(linha);
        }
    }


    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        
        int linha = jTable1.getSelectedRow();
        System.out.println("tela: "+tela);
        System.out.println("linha: "+linha);
        if (tela == 1) {
            lblDescricaoSalao.setText(produtos.get(linha).getDescricao());
        } else if (tela == 3) {
            if (linha < meusProdutos.size()) {
                lblDescricao.setText(meusProdutos.get(linha).getDescricao());
            } else if (linha > meusProdutos.size()) {
                lblDescricao.setText(subscribe.get(linha - meusProdutos.size() - 1).getDescricao());
            }
            else{
                lblDescricao.setText("");
            }
                
        }

    }//GEN-LAST:event_jTable1MouseClicked

